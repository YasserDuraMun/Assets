# Update all Controllers to use new roles

$controllersPath = "Controllers\"

# Define replacements
$replacements = @{
    # For view operations (GET)
    'Authorize\(Roles = "Admin,WarehouseKeeper,Viewer"\)' = 'Authorize(Roles = "Super Admin,Admin,Manager,Employee,Viewer")'
    
    # For create/update operations  
    'Authorize\(Roles = "Admin,WarehouseKeeper"\)' = 'Authorize(Roles = "Super Admin,Admin,Manager")'
    
    # For admin-only operations (but not delete)
    'Authorize\(Roles = "Admin"\)' = 'Authorize(Roles = "Super Admin,Admin,Manager")'
    
    # For delete operations (will be handled manually)
    # 'Authorize\(Roles = "Admin"\).*Delete' = 'Authorize(Roles = "Super Admin,Admin")'
}

# Get all controller files except certain ones
$excludeControllers = @(
    "DashboardController.cs",
    "SecurityTestController.cs", 
    "AuthController.cs",
    "HealthController.cs"
)

$controllerFiles = Get-ChildItem -Path $controllersPath -Filter "*Controller.cs" | 
    Where-Object { $_.Name -notin $excludeControllers }

Write-Host "?? ????? Controllers ??????? ???????" -ForegroundColor Green
Write-Host "================================" -ForegroundColor Green
Write-Host ""

foreach ($file in $controllerFiles) {
    Write-Host "?? ??????: $($file.Name)" -ForegroundColor Yellow
    
    $content = Get-Content $file.FullName -Raw
    $originalContent = $content
    
    # Apply replacements
    foreach ($pattern in $replacements.Keys) {
        $replacement = $replacements[$pattern]
        if ($content -match $pattern) {
            Write-Host "  ? ?? ?????? ??? ??? ???? ????? ????????" -ForegroundColor Cyan
            $content = $content -replace $pattern, $replacement
        }
    }
    
    # Save if changes were made
    if ($content -ne $originalContent) {
        Set-Content -Path $file.FullName -Value $content -Encoding UTF8
        Write-Host "  ?? ?? ??? ?????????" -ForegroundColor Green
    } else {
        Write-Host "  ?? ?? ???? ??????? ??????" -ForegroundColor Gray
    }
    
    Write-Host ""
}

