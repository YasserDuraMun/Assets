#!/usr/bin/env pwsh

# Quick Migration Application - Remove Capacity from Warehouses
# ????? ???? ????????? ??? ????? ?? ??????????

Write-Host "?? ????? ???????? ??? ????? ?? ??????????" -ForegroundColor Green

# Try EF Core first
Write-Host "`n1?? ?????? ????? ???????? EF Core..." -ForegroundColor Yellow
try {
    dotnet ef database update
    
    if ($LASTEXITCODE -eq 0) {
        Write-Host "? ?? ????? Migration ????? ???????? EF Core" -ForegroundColor Green
    } else {
        throw "EF Core migration failed"
    }
} catch {
    Write-Host "?? ??? EF Core? ?????? ??????? SQL Script..." -ForegroundColor Yellow
    
    # Fallback to SQL Script
    Write-Host "`n2?? ????? ???????? SQL Script..." -ForegroundColor Yellow
    
    $sqlScriptPath = "Scripts\RemoveCapacityFromWarehouses.sql"
    
    if (Test-Path $sqlScriptPath) {
        try {
            # Read connection string from appsettings.json
            $appsettings = Get-Content "appsettings.json" | ConvertFrom-Json
            $connectionString = $appsettings.ConnectionStrings.DefaultConnection
            
            # Execute SQL script
            $sqlScript = Get-Content $sqlScriptPath -Raw
            Invoke-Sqlcmd -ConnectionString $connectionString -Query $sqlScript
            
            Write-Host "? ?? ????? SQL Script ?????" -ForegroundColor Green
        } catch {
            Write-Host "? ??? ????? SQL Script: $($_.Exception.Message)" -ForegroundColor Red
            Write-Host "`n?? ???? ????? ??? SQL Script ??????:" -ForegroundColor Yellow
            Write-Host "   1. ???? SQL Server Management Studio" -ForegroundColor Cyan
            Write-Host "   2. ???? ?????? ????????" -ForegroundColor Cyan
            Write-Host "   3. ??? ??????? ?????: Scripts\RemoveCapacityFromWarehouses.sql" -ForegroundColor Cyan
        }
    } else {
        Write-Host "? ??? SQL Script ??? ?????: $sqlScriptPath" -ForegroundColor Red
    }
}

Write-Host "`n?? ?? ???????? ?? ????? Migration" -ForegroundColor Green