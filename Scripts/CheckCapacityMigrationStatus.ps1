#!/usr/bin/env pwsh

# Check Migration Status for Capacity Removal
# ??? ???? ???????? ??? ?????

Write-Host "?? ??? ???? ???????? ??? ????? ?? ??????????" -ForegroundColor Green
Write-Host "=============================================" -ForegroundColor Green

try {
    # Read connection string
    $appsettings = Get-Content "appsettings.json" | ConvertFrom-Json
    $connectionString = $appsettings.ConnectionStrings.DefaultConnection
    
    # Check if Capacity column exists
    $checkCapacityQuery = @"
SELECT 
    CASE WHEN EXISTS (
        SELECT * FROM INFORMATION_SCHEMA.COLUMNS 
        WHERE TABLE_NAME = 'Warehouses' 
        AND COLUMN_NAME = 'Capacity'
    ) THEN 1 ELSE 0 END AS CapacityExists
"@
    
    # Check if migration is recorded
    $checkMigrationQuery = @"
SELECT 
    CASE WHEN EXISTS (
        SELECT * FROM __EFMigrationsHistory 
        WHERE MigrationId LIKE '%RemoveCapacityFromWarehouse%'
    ) THEN 1 ELSE 0 END AS MigrationRecorded
"@
    
    $capacityResult = Invoke-Sqlcmd -ConnectionString $connectionString -Query $checkCapacityQuery
    $migrationResult = Invoke-Sqlcmd -ConnectionString $connectionString -Query $checkMigrationQuery
    
    Write-Host "`n?? ????? ?????:" -ForegroundColor Cyan
    
    if ($capacityResult.CapacityExists -eq 0) {
        Write-Host "? ???? ?????: ?????" -ForegroundColor Green
    } else {
        Write-Host "? ???? ?????: ????? (????? ?????)" -ForegroundColor Red
    }
    
    if ($migrationResult.MigrationRecorded -eq 1) {
        Write-Host "? ??? Migration: ????" -ForegroundColor Green
    } else {
        Write-Host "?? ??? Migration: ??? ????" -ForegroundColor Yellow
    }
    
    # Show current warehouse table structure
    Write-Host "`n?? ???? ???? ?????????? ??????:" -ForegroundColor Cyan
    
    $structureQuery = @"
SELECT 
    COLUMN_NAME as '??? ??????',
    DATA_TYPE as '??? ????????',
    IS_NULLABLE as '???? NULL',
    COLUMN_DEFAULT as '?????? ??????????'
FROM INFORMATION_SCHEMA.COLUMNS 
WHERE TABLE_NAME = 'Warehouses' 
ORDER BY ORDINAL_POSITION
"@
    
    $structure = Invoke-Sqlcmd -ConnectionString $connectionString -Query $structureQuery
    $structure | Format-Table -AutoSize
    
    # Recommendations
    Write-Host "`n?? ????????:" -ForegroundColor Yellow
    
    if ($capacityResult.CapacityExists -eq 1) {
        Write-Host "?? ????? ????? Migration ???? ???? ?????" -ForegroundColor Red
        Write-Host "   ?????: .\Scripts\QuickApplyCapacityRemoval.ps1" -ForegroundColor Cyan
    } else {
        Write-Host "?? ?????????? ???? ????? - ?? ???? ???????? ??????" -ForegroundColor Green
    }
    
} catch {
    Write-Host "? ??? ?? ??????? ?????? ????????: $($_.Exception.Message)" -ForegroundColor Red
    Write-Host "`n?? ???? ??:" -ForegroundColor Yellow
    Write-Host "   • ????? SQL Server" -ForegroundColor Cyan
    Write-Host "   • ??? connection string ?? appsettings.json" -ForegroundColor Cyan
    Write-Host "   • ???? ????? ????????" -ForegroundColor Cyan
}

Write-Host "`n? ????? ?????" -ForegroundColor Green