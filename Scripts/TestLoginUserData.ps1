#!/usr/bin/env pwsh

# Test Login and User Data - Debug User Information Display
# ?????? ????? ?????? ???? ?????? ????????

$baseUrl = "http://localhost:5002/api"

Write-Host "?? ?????? ????? ?????? ???? ?????? ????????" -ForegroundColor Green
Write-Host "=================================================" -ForegroundColor Green

# Test login
Write-Host "`n1?? ?????? ????? ??????..." -ForegroundColor Yellow

$loginData = @{
    username = "admin"
    password = "Admin@123"
} | ConvertTo-Json

Write-Host "?? ?????? ????? ??????:" -ForegroundColor Cyan
Write-Host "$loginData" -ForegroundColor White

try {
    $loginResponse = Invoke-RestMethod -Uri "$baseUrl/auth/login" -Method POST -Body $loginData -ContentType "application/json"
    
    if ($loginResponse.success) {
        Write-Host "? ?? ????? ?????? ?????!" -ForegroundColor Green
        
        $userData = $loginResponse.data
        
        Write-Host "`n?? ?????? ??????? ????? ??????:" -ForegroundColor Cyan
        Write-Host "   ?? Token: $($userData.token)" -ForegroundColor White
        Write-Host "   ?? Username: $($userData.username)" -ForegroundColor White
        Write-Host "   ?? FullName: $($userData.fullName)" -ForegroundColor White
        Write-Host "   ?? Role: $($userData.role)" -ForegroundColor White
        Write-Host "   ? Expires At: $($userData.expiresAt)" -ForegroundColor White
        
        # Check what should be displayed in frontend
        Write-Host "`n??? ?? ??? ?? ???? ?? Frontend:" -ForegroundColor Yellow
        
        if ($userData.fullName) {
            Write-Host "   ? ??? ????????: $($userData.fullName)" -ForegroundColor Green
        } elseif ($userData.username) {
            Write-Host "   ?? ???????? Username: $($userData.username)" -ForegroundColor Yellow
        } else {
            Write-Host "   ? ?? ???? ?????? ??? ????????!" -ForegroundColor Red
        }
        
        # Test what gets stored in localStorage (simulate)
        Write-Host "`n?? ????? localStorage ???????:" -ForegroundColor Cyan
        $simulatedUserData = @{
            username = $userData.username
            fullName = $userData.fullName
            role = $userData.role
            expiresAt = $userData.expiresAt
        }
        
        $simulatedJson = $simulatedUserData | ConvertTo-Json -Compress
        Write-Host "user: $simulatedJson" -ForegroundColor White
        Write-Host "token: $($userData.token)" -ForegroundColor White
        
        # Verify field accessibility
        Write-Host "`n?? ???? ?? ??????:" -ForegroundColor Cyan
        
        $checkFields = @('username', 'fullName', 'role', 'token')
        foreach ($field in $checkFields) {
            $value = $userData.$field
            if ($value) {
                Write-Host "   ? $field = '$value'" -ForegroundColor Green
            } else {
                Write-Host "   ? $field = <empty or null>" -ForegroundColor Red
            }
        }
        
    } else {
        Write-Host "? ??? ????? ??????: $($loginResponse.message)" -ForegroundColor Red
    }
} catch {
    Write-Host "? ??? ?? ????? ??????: $($_.Exception.Message)" -ForegroundColor Red
    if ($_.Exception.Response) {
        try {
            $errorStream = $_.Exception.Response.GetResponseStream()
            $reader = New-Object System.IO.StreamReader($errorStream)
            $errorBody = $reader.ReadToEnd()
            Write-Host "   ?? ?????? ?????: $errorBody" -ForegroundColor Red
        } catch {
            Write-Host "   ?? ?? ???? ????? ?????? ?????" -ForegroundColor Red
        }
    }
}

Write-Host "`n?? ????? ??????? ?? Frontend:" -ForegroundColor Yellow
Write-Host "   1. ???? Developer Tools (F12)" -ForegroundColor Cyan
Write-Host "   2. ???? ??? Console tab" -ForegroundColor Cyan
Write-Host "   3. ??? ???? ????? ??????? ???????:" -ForegroundColor Cyan
Write-Host "      - '?? Login Response Data:'" -ForegroundColor White
Write-Host "      - '?? User FullName:'" -ForegroundColor White
Write-Host "      - '?? AuthContext useEffect'" -ForegroundColor White
Write-Host "      - '? Parsed User Data:'" -ForegroundColor White
Write-Host "   4. ???? ?? localStorage:" -ForegroundColor Cyan
Write-Host "      - ???? F12 ? Application ? Local Storage" -ForegroundColor White
Write-Host "      - ???? ?? 'user' ? 'token'" -ForegroundColor White

Write-Host "`n?? ???? ??????:" -ForegroundColor Yellow
Write-Host "   • ??? ??? fullName ???? ? ???? ?? ????? ????????" -ForegroundColor Cyan
Write-Host "   • ??? ??? JSON ??? ???? ? ???? ?? AuthContext" -ForegroundColor Cyan
Write-Host "   • ??? ??? ??????? ?? ???? ? ???? ?? ????? ?????????" -ForegroundColor Cyan

Write-Host "`n?? ????? ?????? ????? ??????" -ForegroundColor Green