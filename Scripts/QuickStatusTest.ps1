#!/usr/bin/env pwsh

# Quick Status System Test
# ?????? ???? ????? ????? ??????

$baseUrl = "http://localhost:5002/api"

Write-Host "?? ?????? ?????? ??????? ?????? ??????" -ForegroundColor Green
Write-Host "=========================================" -ForegroundColor Green

# Login
$loginData = @{
    Username = "admin"
    Password = "Admin@123"
} | ConvertTo-Json

Write-Host "`n1?? ?????? ????? ??????..." -ForegroundColor Yellow
try {
    $loginResponse = Invoke-RestMethod -Uri "$baseUrl/auth/login" -Method POST -Body $loginData -ContentType "application/json"
    $token = $loginResponse.data.token
    Write-Host "? ?? ????? ?????? ?????" -ForegroundColor Green
} catch {
    Write-Host "? ??? ????? ??????: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}

$headers = @{
    "Authorization" = "Bearer $token"
    "Content-Type" = "application/json"
}

# Test creating status WITHOUT code field
Write-Host "`n2?? ????? ???? ????? ???? ??? (????? ????????)..." -ForegroundColor Yellow
$newStatus = @{
    name = "???? ??????? ?????"
    description = "?????? ?????? ?????? - ????? ????? ????????"
    color = "#2196F3"
} | ConvertTo-Json

try {
    $createResponse = Invoke-RestMethod -Uri "$baseUrl/statuses" -Method POST -Body $newStatus -Headers $headers
    $createdId = $createResponse.data.id
    $generatedCode = $createResponse.data.code
    Write-Host "? ?? ????? ?????? ?????" -ForegroundColor Green
    Write-Host "   ?? ID: $createdId" -ForegroundColor Cyan
    Write-Host "   ??? ????? ??????? ????????: $generatedCode" -ForegroundColor Cyan
} catch {
    Write-Host "? ??? ????? ??????: $($_.Exception.Message)" -ForegroundColor Red
    $createdId = $null
}

if ($createdId) {
    # Test updating status
    Write-Host "`n3?? ????? ?????? (???? ???? ?? ?????)..." -ForegroundColor Yellow
    $updateStatus = @{
        id = $createdId
        name = "???? ????? - ????? ????? ????????"
        description = "?? ??????? ????? ?????? ?? ?????? ????????"
        color = "#4CAF50"
        isActive = $true
    } | ConvertTo-Json

    try {
        $updateResponse = Invoke-RestMethod -Uri "$baseUrl/statuses/$createdId" -Method PUT -Body $updateStatus -Headers $headers
        $newCode = $updateResponse.data.code
        Write-Host "? ?? ????? ?????? ?????" -ForegroundColor Green
        Write-Host "   ??? ????? ??????: $newCode" -ForegroundColor Cyan
    } catch {
        Write-Host "? ??? ????? ??????: $($_.Exception.Message)" -ForegroundColor Red
        if ($_.Exception.Response) {
            $errorDetails = $_.Exception.Response.GetResponseStream()
            $reader = New-Object System.IO.StreamReader($errorDetails)
            $errorBody = $reader.ReadToEnd()
            Write-Host "   ?? ?????? ?????: $errorBody" -ForegroundColor Red
        }
    }

    # Verify the status exists and has proper data
    Write-Host "`n4?? ?????? ?? ????????..." -ForegroundColor Yellow
    try {
        $statusResponse = Invoke-RestMethod -Uri "$baseUrl/statuses/$createdId" -Method GET -Headers $headers
        Write-Host "? ?? ??????? ?????? ?????:" -ForegroundColor Green
        Write-Host "   ?? ?????: $($statusResponse.data.name)" -ForegroundColor Cyan
        Write-Host "   ??? ?????: $($statusResponse.data.code)" -ForegroundColor Cyan
        Write-Host "   ?? ?????: $($statusResponse.data.description)" -ForegroundColor Cyan
        Write-Host "   ?? ?????: $($statusResponse.data.color)" -ForegroundColor Cyan
        Write-Host "   ? ???: $($statusResponse.data.isActive)" -ForegroundColor Cyan
    } catch {
        Write-Host "? ??? ??????? ??????: $($_.Exception.Message)" -ForegroundColor Red
    }

    # Clean up
    Write-Host "`n5?? ????? ???????? ?????????..." -ForegroundColor Yellow
    try {
        Invoke-RestMethod -Uri "$baseUrl/statuses/$createdId" -Method DELETE -Headers $headers | Out-Null
        Write-Host "? ?? ??? ?????? ?????????" -ForegroundColor Green
    } catch {
        Write-Host "?? ???? ??? ?????? ????????? (???? ????? ??????)" -ForegroundColor Yellow
    }
}

# Test getting all statuses
Write-Host "`n6?? ??????? ???? ???????..." -ForegroundColor Yellow
try {
    $allStatuses = Invoke-RestMethod -Uri "$baseUrl/statuses" -Method GET -Headers $headers
    Write-Host "? ?? ??????? $($allStatuses.data.Count) ????" -ForegroundColor Green
    
    if ($allStatuses.data.Count -gt 0) {
        Write-Host "`n?? ????? ??????? ????????:" -ForegroundColor Cyan
        foreach ($status in $allStatuses.data) {
            $activeStatus = if ($status.isActive) { "?" } else { "?" }
            Write-Host "   $activeStatus $($status.name) (???: $($status.code))" -ForegroundColor White
        }
    }
} catch {
    Write-Host "? ??? ??????? ???????: $($_.Exception.Message)" -ForegroundColor Red
}

Write-Host "`n?? ????? ????????!" -ForegroundColor Green
Write-Host "=========================================" -ForegroundColor Green
